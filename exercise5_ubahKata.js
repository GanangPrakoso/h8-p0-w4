function ubahHuruf(kata) {
    var henshin='';
    for (let i=0; i<=kata.length-1; i++){
        if (kata[i]=='a'){
            henshin+='b';
        }
        if (kata[i]=='b'){
            henshin+='c';
        }
        if (kata[i]=='c'){
            henshin+='d';
        }
        if (kata[i]=='d'){
            henshin+='e';
        }
        if (kata[i]=='e'){
            henshin+='f';
        }
        if (kata[i]=='f'){
            henshin+='g';
        }
        if (kata[i]=='g'){
            henshin+='h';
        }
        if (kata[i]=='h'){
            henshin+='i';
        }
        if (kata[i]=='i'){
            henshin+='j';
        }
        if (kata[i]=='j'){
            henshin+='k';
        }
        if (kata[i]=='k'){
            henshin+='l';
        }
        if (kata[i]=='l'){
            henshin+='m';
        }
        if (kata[i]=='m'){
            henshin+='n';
        }
        if (kata[i]=='n'){
            henshin+='o';
        }
        if (kata[i]=='o'){
            henshin+='p';
        }
        if (kata[i]=='p'){
            henshin+='q';
        }
        if (kata[i]=='q'){
            henshin+='r';
        }
        if (kata[i]=='r'){
            henshin+='s';
        }
        if (kata[i]=='s'){
            henshin+='t';
        }
        if (kata[i]=='t'){
            henshin+='u';
        }
        if (kata[i]=='u'){
            henshin+='v';
        }
        if (kata[i]=='v'){
            henshin+='w';
        }
        if (kata[i]=='w'){
            henshin+='x';
        }
        if (kata[i]=='x'){
            henshin+='y';
        }
        if (kata[i]=='y'){
            henshin+='z';
        }
        if (kata[i]=='z'){
            henshin+='a';
        }
    }
    return henshin;
  }
  
  console.log(ubahHuruf('wow')); // xpx
  console.log(ubahHuruf('developer')); // efwfmpqfs
