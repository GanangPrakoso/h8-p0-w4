function countProfit(shoppers) {
    let listBarang = [ ['Sepatu Stacattu', 1500000, 10],
                       ['Baju Zoro', 500000, 2],
                       ['Sweater Uniklooh', 175000, 1]
                     ];
  
    if (shoppers.length == 0){
        return [];
    }

    var tokoX = [];
    for ( let i=0; i<=listBarang.length-1; i++){
    var objToko = {};
    var hedonist = []
    var leftOver = 0;

        for (let j=0; j<=shoppers.length-1; j++){
            if (shoppers[j].product === listBarang[i][0] && leftOver + shoppers[j].amount <= listBarang[i][2]){
                leftOver+=shoppers[j].amount;

                hedonist.push(shoppers[j].name)
            }
        }
        objToko['product'] = listBarang[i][0];
        objToko['shoppers'] = hedonist;
        objToko['leftover'] = listBarang[i][2] - leftOver;
        objToko['totalProfit'] = listBarang[i][1]*leftOver

        tokoX.push(objToko);
    }
    console.log('');
    
    return tokoX;
  }
  
